# Python Positive & Negative Indexing
# Python uses zero-based Indexing
fruits = ['apple','banana','cherry','plum','grapes']
print(fruits)
print('-' * 50)
print("Positive Indexing (left->right, starts at 0)")
print(fruits[0])
print(fruits[1])
print(fruits[2])
print(fruits[3])
print(fruits[4])
print('-' * 40)

print("Negative Indexing (right->left,starts at -1)")
print(fruits[-1])
print(fruits[-2])
print(fruits[-3])
print(fruits[-4])
print(fruits[-5])
print('-' * 40)

